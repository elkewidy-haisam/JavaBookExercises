package ChapterTwo;

public class TwoPointEighteen {
	
	public static void main(String[] args) {
		
		int a = 1;
		int b = 2;
		
		System.out.println("a     b     pow(a, b)");
		
		for (int i = 1; i <= 5; i++) {
			
			System.out.println(a + "     " + b + "     " + Math.pow(a, b));
			a++;
			b++;
			
			
			
		}
		
		
	}

}
