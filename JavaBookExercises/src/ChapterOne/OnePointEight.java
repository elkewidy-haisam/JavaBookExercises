package ChapterOne;

public class OnePointEight {
	
	private static double radius = 5.5;
	private static double area = Math.PI * Math.pow(radius, 2) ;
	private static double perimeter = 2 * Math.PI * radius;
	
	
	public static void main(String[] args) {
		
		System.out.println(area);
		System.out.println(perimeter);
		
		
	}

}
